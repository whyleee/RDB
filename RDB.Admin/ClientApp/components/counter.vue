<template>
  <div>
    <h1>Counter</h1>
    <p>This is a simple example of a Vue.js component.</p>
    <p>Current count: <strong>{{count}}</strong></p>

    <button @click="increment">Increment</button>
  </div>
</template>

<script>
import { mapState, mapMutations } from 'vuex'

export default {
  computed: {
    ...mapState('counter', [
      'count'
    ])
  },
  methods: {
    ...mapMutations('counter', [
      'increment'
    ])
  }
}
</script>
