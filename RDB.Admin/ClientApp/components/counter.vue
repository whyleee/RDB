<template>
  <div>
    <h1>Counter</h1>
    <p>This is a simple example of a Vue.js component.</p>
    <p>Current count: <strong>{{count}}</strong></p>

    <button @click="increment">Increment</button>
  </div>
</template>

<script>
export default {
  data() {
    return {
      count: 0
    }
  },
  methods: {
    increment() {
      this.count += 1
    }
  }
}
</script>
